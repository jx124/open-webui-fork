import { WEBUI_API_BASE_URL } from '$lib/constants';

export type EvaluationForm = {
	id: number,
    title: string,
    content: string,
}

export const createNewEvaluation = async (
	token: string,
	form_data: EvaluationForm
) => {
	let error = null;

	const res = await fetch(`${WEBUI_API_BASE_URL}/evaluations/create`, {
		method: 'POST',
		headers: {
			Accept: 'application/json',
			'Content-Type': 'application/json',
			authorization: `Bearer ${token}`
		},
		body: JSON.stringify(form_data)
	})
		.then(async (res) => {
			if (!res.ok) throw await res.json();
			return res.json();
		})
		.catch((err) => {
			error = err.detail;
			console.log(err);
			return null;
		});

	if (error) {
		throw error;
	}

	return res;
};

